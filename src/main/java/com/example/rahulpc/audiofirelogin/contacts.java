package com.example.rahulpc.audiofirelogin;



public class contacts {

    String name;
    String phoneNumber;

    public contacts(String name, String phoneNumber) {
        this.name = name;
        this.phoneNumber = phoneNumber;
    }

    public String getName() {
        return name;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }


}
